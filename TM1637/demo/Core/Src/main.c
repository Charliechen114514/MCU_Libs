void adjust_num(uint8_t disp[]){
  // the two and third one is seconds
  disp[3]++;
  if(disp[3] >= 10){
    disp[2] += 1;
    disp[3] = 0;
  }

  if(disp[2] >= 6)
  {
    disp[1] += 1;
    disp[2] = 0;
  }

    if(disp[1] >= 10){
    disp[0] += 1;
    disp[1] = 0;
  }
}

